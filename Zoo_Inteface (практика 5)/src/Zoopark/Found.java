    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //Записываем в переменные данные из полей формы
        String Name = jTextField1.getText();
        String Type = jTextField2.getText();
        String Cls = jTextField3.getText();
        String Voice = jTextField4.getText();
        String Room = jTextField5.getText();
        
        String temp = "";
        //Добавляем животное 
        temp = Zoopark.AddAnimal(Name, Type, Cls, Voice, Room);
        
        
        if ( !temp.equals("Error") ) //темп не равен еррор
        {
            temp = "Добавлено. " + Room + ": " + Name + ", " + Type + ", " + Cls + ", " + Voice + '\n';
        }
        else //Если функция вернула ошибку
        {
            temp = "Занято";
        }

        jTextArea2.append(temp + '\n'); //Выводим на форму
    }//GEN-LAST:event_jButton1ActionPerformed

